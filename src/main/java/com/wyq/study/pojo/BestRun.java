    public Double getHalfMarathonPb() {
        return halfMarathonPb;
    }

    public void setHalfMarathonPb(Double halfMarathonPb) {
        this.halfMarathonPb = halfMarathonPb;
    }

    public Double getFullMarathonPb() {
        return fullMarathonPb;
    }

    public void setFullMarathonPb(Double fullMarathonPb) {
        this.fullMarathonPb = fullMarathonPb;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }
